const url = "https://www.thomasnoroffproject.site/wp-json/wp/v2/posts";

const resultsContainer = document.querySelector(".post1");


async function getPosts() {
    fetch

    const response = await fetch(url);

    const posts = await response.json();

    resultsContainer.innerHTML = "";

    for (let i = 0; i < posts.length; i++) {

        if (i === 1) {
            break;
        }

        resultsContainer.innerHTML += `<div class="post1">${posts[i].title.rendered} \n ${posts[i].excerpt.rendered}</div>`;
    }
}

